   public void preencherTabela(String Sql){
       ArrayList dados = new ArrayList ();
       String [] colunas = new String[] {"nome", "email", "cpf"};
       conexao.conecta();
       conexao.executaSQL (Sql);
       
       try {
           conexao.rs.first();
           do{
               dados.add(new Object [] {conexao.rs.getString("nome"), conexao.rs.getString ("email"),
               conexao.rs.getString("cpf")});
               
           }while (conexao.rs.next());
       }catch(SQLException ex){
           JOptionPane.showMessageDialog(rootPane, "Erro ao preencher o ARRAYLIST"+ex);
       }
       mostraTabela tabela = new mostraTabela (dados, colunas);
       jTable1.setModel(tabela);
       jTable1.getColumnModel().getColumn(0).setPreferredWidth(100);       
       jTable1.getColumnModel().getColumn(0).setResizable(false);
       jTable1.getColumnModel().getColumn(1).setPreferredWidth(200); 
       jTable1.getColumnModel().getColumn(1).setResizable(false);       
       jTable1.getColumnModel().getColumn(2).setPreferredWidth(200);       
       jTable1.getColumnModel().getColumn(2).setResizable(false);
       jTable1.getTableHeader().setReorderingAllowed(false);
       jTable1.setAutoResizeMode (jTable1.AUTO_RESIZE_OFF);
       jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       conexao.desconecta();
               
       
       
       
   }
   
   
    
